package arithmeticOperatorsExamples;

public class ArithmeticOperationsExample {
	public static void main(String args[]) {
	      int num1 = 100;
	      int num2 = 20;

	      System.out.println("On addition value is:" + (num1 + num2) );
	      System.out.println("On subtraction value is:" + (num1 - num2) );
	      System.out.println("On multiplication value is:" + (num1 * num2) ); 
	      System.out.println("On division value is:" + (num1 / num2) );
	      System.out.println("On modulo value is:" + (num1 % num2) );
	   }

}
